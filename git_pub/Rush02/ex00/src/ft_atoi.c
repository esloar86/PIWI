#include <rush02_lib.h>

int		ft_atoi(char *str)
{
	int					i;
	unsigned int		nb;

	nb = 0;
	i = 0;

	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (-1);
		else
			i++;
	}
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	if (i == 0 || nb > 4294967295)
		return (-1);
	
	return (nb);
}

